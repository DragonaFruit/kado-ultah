import time
import sys
import os
import random

# Fix Encoding UTF-8 untuk Windows Terminal/CMD
if os.name == 'nt':
    os.system('chcp 65001 > nul')
    if hasattr(sys.stdout, 'reconfigure'):
        sys.stdout.reconfigure(encoding='utf-8')

def ketik_efek(teks, kecepatan=0.05):
    for karakter in teks:
        sys.stdout.write(karakter)
        sys.stdout.flush()
        time.sleep(kecepatan)
    print()

def cetak_ascii_smooth(ascii_text, jeda_baris=0.035):
    for baris in ascii_text.split('\n'):
        print(baris)
        time.sleep(jeda_baris)

def animasi_hati_berkelip(ascii_text):
    os.system('cls' if os.name == 'nt' else 'clear')
    symbols = ['✨', '⭐', '❤️', '💖', '💕', '🌸', '  ', '  ']
    lines = ascii_text.strip('\n').split('\n')
    
    print("\n" * 2)
    for line in lines:
        hiasan_kiri = "".join(random.choice(symbols) for _ in range(3))
        hiasan_kanan = "".join(random.choice(symbols) for _ in range(3))
        print(f"  {hiasan_kiri}   {line}   {hiasan_kanan}")
    
    print("\n" + " " * 15 + "✨ ⭐ ❤️  HAPPY BIRTHDAY BIBUB  ❤️ ⭐ ✨")
    print("\n" + " " * 12 + "👉 [ Tekan ENTER di keyboard untuk keluar ] 👈")

